package parser.yaml_parser;

public class DevDependencies {
	
		private String test;
	
		public DevDependencies() {
			super();
		}

		public DevDependencies(String test) {
			super();
			this.test = test;
		}


		public String getTest() {
			return test;
		}

		public void setTest(String test) {
			this.test = test;
		}
}
